// LOCKING
// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
async function claimRows(limit) {
  const cutoff = new Date(Date.now() - LOCK_TTL_MIN * 60 * 1000).toISOString();

  // 1. Free expired locks
  await supabase
    .from("flashcard_raw")
    .update({ phase_json_lock: null, phase_json_lock_at: null })
    .is("phase_json", null)
    .lt("phase_json_lock_at", cutoff);

  // 2. Select candidates ‚Äî rows that HAVE concept but no phase_json yet
  const { data: candidates, error: e1 } = await supabase
    .from("flashcard_raw")
    .select("id, concept")
    .not("concept", "is", null)
    .not("concept", "eq", "")
    .is("phase_json", null)
    .is("phase_json_lock", null)
    .order("id", { ascending: true })
    .limit(limit);

  if (e1) throw e1;
  if (!candidates?.length) return [];

  const ids = candidates.map(r => r.id);

  // 3. Lock claimed rows
  const { data: locked, error: e2 } = await supabase
    .from("flashcard_raw")
    .update({
      phase_json_lock: WORKER_ID,
      phase_json_lock_at: new Date().toISOString()
    })
    .in("id", ids)
    .is("phase_json", null)
    .is("phase_json_lock", null)
    .select("id, concept");

  if (e2) throw e2;
  return locked || [];
}

async function clearLocks(ids) {
  if (!ids.length) return;
  await supabase
    .from("flashcard_raw")
    .update({ phase_json_lock: null, phase_json_lock_at: null })
    .in("id", ids);
}

